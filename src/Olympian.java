
public class Olympian {

String name;
String sport;
String gender;
int age;
String country;
int numMedals;

Olympian (String name, String sport, String gender, int age, String country, int numMedals){
	
	this.name = name;
	this.sport = sport;
	this.gender = gender;
	this.age = age;
	this.country = country;
	this.numMedals = numMedals;
	
}

}
